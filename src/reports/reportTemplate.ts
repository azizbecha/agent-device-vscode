const REPORT_JS = `
(function () {
  const search = document.getElementById('search');
  const chips = Array.from(document.querySelectorAll('.chip[data-filter]'));
  const toggleAll = document.getElementById('toggle-all');
  const noResults = document.getElementById('no-results');
  const steps = Array.from(document.querySelectorAll('li.step'));
  let activeFilter = 'all';

  function applyFilters() {
    const query = (search.value || '').trim().toLowerCase();
    let visible = 0;
    for (const step of steps) {
      const stateMatch = activeFilter === 'all' || step.dataset.state === activeFilter;
      const haystack = step.dataset.search || '';
      const textMatch = !query || haystack.indexOf(query) !== -1;
      const show = stateMatch && textMatch;
      step.hidden = !show;
      if (show) visible++;
    }
    if (noResults) noResults.hidden = visible > 0;
  }

  search.addEventListener('input', applyFilters);
  search.addEventListener('keydown', function (e) { if (e.key === 'Escape') { search.value = ''; applyFilters(); } });

  chips.forEach(function (chip) {
    chip.addEventListener('click', function () {
      activeFilter = chip.dataset.filter || 'all';
      chips.forEach(function (c) { c.setAttribute('aria-selected', String(c === chip)); });
      applyFilters();
    });
  });

  if (toggleAll) {
    toggleAll.addEventListener('click', function () {
      const allDetails = document.querySelectorAll('details');
      const expand = toggleAll.getAttribute('aria-pressed') !== 'true';
      allDetails.forEach(function (d) { d.open = expand; });
      toggleAll.setAttribute('aria-pressed', String(expand));
      toggleAll.textContent = expand ? 'Collapse all' : 'Expand all';
    });
  }

  document.addEventListener('keydown', function (e) {
    if (e.key === '/' && document.activeElement !== search && !e.metaKey && !e.ctrlKey) {
      e.preventDefault();
      search.focus();
      search.select();
    }
  });

  document.addEventListener('click', function (e) {
    const target = e.target;
    if (!(target instanceof HTMLElement)) return;
    const btn = target.closest('.copy-btn');
    if (!btn) return;
    e.preventDefault();
    e.stopPropagation();
    let text = '';
    if (btn.dataset.copy != null) {
      text = btn.dataset.copy;
    } else if (btn.hasAttribute('data-copy-target')) {
      const details = btn.closest('details');
      const pre = details && details.querySelector('pre');
      if (pre) text = pre.textContent || '';
    }
    if (!text) return;
    Promise.resolve(navigator.clipboard.writeText(text))
      .then(function () { flashCopied(btn); })
      .catch(function () { flashCopied(btn, 'failed'); });
  });

  function flashCopied(btn, mode) {
    const original = btn.textContent;
    btn.classList.add('copied');
    btn.textContent = mode === 'failed' ? '✗ failed' : '✓ copied';
    setTimeout(function () {
      btn.classList.remove('copied');
      btn.textContent = original;
    }, 1200);
  }
})();
`;
