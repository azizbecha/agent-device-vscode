import { formatDuration } from '../util/duration';
import { pluralize } from '../util/pluralize';

export type ReportStepState = 'passed' | 'failed' | 'skipped';
export type ReportStatus = 'success' | 'failure' | 'cancelled';

export interface ReportStep {
  readonly index: number;
  readonly lineNumber: number;
  readonly display: string;
  readonly state: ReportStepState;
  readonly durationMs?: number;
  readonly stdout?: string;
  readonly stderr?: string;
  readonly errorMessage?: string;
}

export interface ReportData {
  readonly scriptName: string;
  readonly scriptPath: string;
  readonly startedAt: number;
  readonly durationMs: number;
  readonly status: ReportStatus;
  readonly steps: readonly ReportStep[];
}

export function renderReport(data: ReportData): string {
  const passed = data.steps.filter((s) => s.state === 'passed').length;
  const failed = data.steps.filter((s) => s.state === 'failed').length;
  const skipped = data.steps.filter((s) => s.state === 'skipped').length;
  const startedIso = new Date(data.startedAt).toISOString();
  const startedHuman = new Date(data.startedAt).toLocaleString();

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8" />
  <meta name="viewport" content="width=device-width, initial-scale=1" />
  <title>Agent Device — ${escapeHtml(data.scriptName)}</title>
  <style>${REPORT_CSS}</style>
</head>
<body data-status="${data.status}">
  <header class="page-header">
    <div class="title">
      <h1>${escapeHtml(data.scriptName)}</h1>
      <span class="badge ${badgeClass(data.status)}">${statusLabel(data.status)}</span>
    </div>
    <dl class="meta">
      <div><dt>Duration</dt><dd>${escapeHtml(formatDuration(data.durationMs))}</dd></div>
      <div><dt>Started</dt><dd><time datetime="${escapeHtml(startedIso)}">${escapeHtml(startedHuman)}</time></dd></div>
      <div><dt>Steps</dt><dd>${pluralize(data.steps.length, 'step')}</dd></div>
      <div><dt>Path</dt><dd><code>${escapeHtml(data.scriptPath)}</code></dd></div>
    </dl>
  </header>

  <div class="toolbar">
    <label class="search">
      <span class="search-icon" aria-hidden="true">⌕</span>
      <input id="search" type="search" placeholder="Filter steps (press / to focus)" autocomplete="off" spellcheck="false" />
      <kbd class="search-hint">/</kbd>
    </label>
    <div class="chips" role="tablist" aria-label="Filter by state">
      <button class="chip" role="tab" aria-selected="true" data-filter="all">All <span class="chip-count">${data.steps.length}</span></button>
      <button class="chip passed" role="tab" aria-selected="false" data-filter="passed">Passed <span class="chip-count">${passed}</span></button>
      <button class="chip failed" role="tab" aria-selected="false" data-filter="failed">Failed <span class="chip-count">${failed}</span></button>
      <button class="chip skipped" role="tab" aria-selected="false" data-filter="skipped">Skipped <span class="chip-count">${skipped}</span></button>
    </div>
    <div class="toolbar-actions">
      <button id="toggle-all" class="ghost" aria-pressed="false">Expand all</button>
    </div>
  </div>

  <main>
    ${data.steps.length === 0 ? '<p class="empty">No steps recorded.</p>' : renderStepList(data.steps)}
    <p id="no-results" class="empty" hidden>No steps match the current filter.</p>
  </main>

  <footer>
    <span>Generated by Agent Device DevTools for VS Code · <a href="https://github.com/azizbecha/agent-device-vscode" target="_blank" rel="noopener">source</a></span>
  </footer>

  <script>${REPORT_JS}</script>
</body>
</html>`;
}

function renderStepList(steps: readonly ReportStep[]): string {
  return `<ol class="steps">${steps.map(renderStep).join('')}</ol>`;
}

function renderStep(step: ReportStep): string {
  const dur = step.durationMs != null ? formatDuration(step.durationMs) : '';
  const body = renderStepBody(step);
  const haystack = `${step.lineNumber} ${step.display} ${step.errorMessage ?? ''}`.toLowerCase();
  return `<li class="step ${step.state}" data-state="${step.state}" data-search="${escapeHtml(haystack)}">
    <div class="step-header">
      <span class="step-icon" aria-label="${step.state}">${stepGlyph(step.state)}</span>
      <span class="step-line">L${step.lineNumber}</span>
      <span class="step-text"><code>${escapeHtml(step.display)}</code></span>
      <span class="step-duration">${escapeHtml(dur)}</span>
      <button class="copy-btn ghost" type="button" data-copy="${escapeHtml(step.display)}" title="Copy command">⧉</button>
    </div>
    ${body}
  </li>`;
}

function renderStepBody(step: ReportStep): string {
  const blocks: string[] = [];
  if (step.errorMessage) {
    blocks.push(detailsBlock('Error', step.errorMessage, true, 'error'));
  }
  if (step.stderr?.trim()) {
    blocks.push(detailsBlock('stderr', step.stderr.trim(), false));
  }
  if (step.stdout?.trim()) {
    blocks.push(detailsBlock('stdout', step.stdout.trim(), false));
  }
  if (blocks.length === 0) {
    return '';
  }
  return `<div class="step-body">${blocks.join('')}</div>`;
}

function detailsBlock(
  label: string,
  text: string,
  openByDefault: boolean,
  extraClass = '',
): string {
  return `<details class="${extraClass}"${openByDefault ? ' open' : ''}>
    <summary>
      <span>${escapeHtml(label)}</span>
      <button class="copy-btn ghost" type="button" data-copy-target>⧉ Copy</button>
    </summary>
    <pre>${escapeHtml(text)}</pre>
  </details>`;
}

function statusLabel(status: ReportStatus): string {
  switch (status) {
    case 'success':
      return 'Pass';
    case 'failure':
      return 'Fail';
    case 'cancelled':
      return 'Cancelled';
  }
}

function badgeClass(status: ReportStatus): string {
  switch (status) {
    case 'success':
      return 'pass';
    case 'failure':
      return 'fail';
    case 'cancelled':
      return 'cancelled';
  }
}

function stepGlyph(state: ReportStepState): string {
  switch (state) {
    case 'passed':
      return '✓';
    case 'failed':
      return '✗';
    case 'skipped':
      return '−';
  }
}

const HTML_ENTITIES: Record<string, string> = {
  '&': '&amp;',
  '<': '&lt;',
  '>': '&gt;',
  '"': '&quot;',
  "'": '&#39;',
};

function escapeHtml(value: string): string {
  return value.replace(/[&<>"']/g, (ch) => HTML_ENTITIES[ch] ?? ch);
}

